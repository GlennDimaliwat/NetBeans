    private void playSong() {
        try {
            if(mediaPlayer!=null) {
                if(mediaPlayer.getStatus().toString().equalsIgnoreCase("PAUSED")) {
                    // If the track is paused
                    mediaPlayer.play();
                    this.setTitle("Glenn Player - Now Playing: "+f.getName());
                }
                else {
                    if(!song.equalsIgnoreCase("") && !mediaPlayer.getStatus().toString().equalsIgnoreCase("PLAYING")) {
                        // If file is opened and the track is not playing
                        playTrack();
                    }
                    else if(!song.equalsIgnoreCase("") && (mediaPlayer.getStopTime().toSeconds()-mediaPlayer.getCurrentTime().toSeconds()<1) ) {
                        // If track is already finished and Play is pressed again
                        stopSong();
                        playTrack();
                    }
                }
                System.out.println(mediaPlayer.getStatus());
            }
            else {
                if(!song.equalsIgnoreCase("")) {
                    playTrack();
                }
            }
        }
        catch(Exception e) {
            System.out.println(e);
        }
    }
    
    private void playTrack() {
        media = new Media(song);
        mediaPlayer = new MediaPlayer(media);
        mediaPlayer.play();                
        this.setTitle("Glenn Player - Now Playing: "+f.getName());
    }
    
