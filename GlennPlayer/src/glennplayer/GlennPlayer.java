    private void pauseSong() {
        try {
            if(mediaPlayer!=null) {
                mediaPlayer.pause();
                this.setTitle("Glenn Player - Paused: "+f.getName());
            }
        }catch(Exception e) {
            System.out.println(e);
        }
    }
    private void stopSong() {
        try {
            if(mediaPlayer!=null) {
                mediaPlayer.stop();
                this.setTitle("Glenn Player");
            }
        }catch(Exception e) {
            System.out.println(e);
        }
    }
    
