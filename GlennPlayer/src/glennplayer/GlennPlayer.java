    private void browseFile() {
        if(jFileChooser1.showOpenDialog(null)==JFileChooser.APPROVE_OPTION) {
            try {
                openFile();
            } catch (MalformedURLException ex) {
                Logger.getLogger(GlennPlayer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
    private void openFile() throws MalformedURLException {
        try {
            if(mediaPlayer!=null) {
                stopSong();
            }
            jTextField1.setText(jFileChooser1.getSelectedFile().toPath().toString());
            f = new File(jTextField1.getText());
            song = f.toURI().toURL().toExternalForm();
            jTextField1.setText(f.getName());
        }
        catch(Exception e) {
            System.out.println(e);
        }
    }
    
